                        <p>
                        外企可以说是留学生们最感兴趣的企业类型之一了，每年有大量的留学生进入或者尝试进入外企工作。选择去外企工作的主要原因，除了期待外企的工作环境、氛围，还有就是能发挥留学的语言优势。虽然很多留学生英语都讲的很溜，但面试毕竟不是一个完全靠临场发挥就能应对的事情，突然抛出的问题加上英语作答，老司机也可能翻车。小编梳理了应聘外企Entry-level岗位时8个常见的面试问题，并整理了回答范例和解题思路。准备去面试外企的同学们可以转发收藏，在面试前充分学习。
                        <p></p>
                        Question1: Tell me about yourself
                        <p></p>
                        【解题思路】外企面试官的思维和习惯与国内面试官有很显著的差异，所以不要照搬中文面试常用的那套自我介绍公式去讲，会显得过于平淡缺乏个性，且很难引起面试官的注意。比较好的方法是能提到一些与职业相关的兴趣爱好。
                        <p></p>
                        【回答范例】The past few years, I’ve gotten really into geocaching. I love the challenge of finding caches and spending time outdoors with friends. I like using my problem-solving skills to find the ones that are really well-hidden. Learning how people hide things, and where people are likely to look, has helped me tremendously in my design work. It’s all about learning to see things through someone else’s eyes.
                        <p></p>
                        【范例解析】这个回答通过介绍自己的经历和兴趣爱好，向面试官展示了从经历中学习的能力、团队协作能力、和敢于冒险拼搏的精神，这些品质对工作能起到重要的影响。
                        <p></p>
                        【特别注意】1) 不要讲的太粗略，也不要过于详细。讲太少会显得放不开，而讲的太多则会太啰嗦，面试官不想了解关于你的每一件事。2) 不要提及一些关于政治、宗教等可能具有争议性的问题见解，因为面试官的看法可能会与你有较大分歧。3) 提及的兴趣爱好要尽可能与求职岗位有相关性。否则面试官会觉得你爱好众多会影响工作时间。4) 避免提到一些个人隐私信息，家庭、孩子、合作伙伴等等。
                        <p></p>
                        Question2: Why do you want to change jobs
                        <p></p>
                        【解题思路】面试官想了解你换工作的原因，并且想确定你不是因为表现不好、同事/上下级关系有问题等原因被迫离开上家公司的。当回答这个问题时重点是让面试官得到一个正面的原因，打消他们怀疑你是否因为糟糕的现状才选择跳槽的疑虑。如果你是被炒而换工作，那更需要提前准备这个问题，尝试把它转化成正面的原因。
                        <p></p>
                        【回答范例】I was lucky enough to land a job at a startup right out of school, which means that I wore many hats right from my first day in the office. Now I’m looking forward to taking my graphic design skills into a senior role.
                        <p></p>
                        【范例解析】求职者先是积极的介绍了自己在一个富有挑战的环境中得到了历练。然后强调了自己换工作的目的是在专业领域更进一步，表达了面试官想要的技能和态度。
                        <p></p>
                        【特别注意】1）将换工作描述为一种职业生涯提升的路径，同时不要诋毁现在的工作。你也可以做好准备，表达清楚这份新工作更加符合自己的长期职业规划。2）可以将一些目前现有的工作的优点。这样既能表达你只是在一个好工作的基础上寻找更好的工作，又能让面试官明白你不是因为陷入窘境而跳槽。3）如果目前的工作状况不好，可以尝试用一些外部原因解释为什么换工作。比如新工作离家更近，通勤更方便等等。
                        <p></p>
                        Question3: Tell me something not on your resume
                        <p></p>
                        【解题思路】外企的面试官更偏向与使用一些open-ended question来考察面试者的应变能力、性格和隐藏能力，而不是全部根据简历提问，这能更好地确认求职者是不是契合岗位和公司。求职者可以通过讲一个简历上没写或隐性的优点、为什么申请这份工作、讲一个有与职业相关的兴趣爱好等来应对。
                        <p></p>
                        【回答范例】You can see from my resume that my sales team exceeded its annual goal by 15%. One of the keys to that success was my willingness to go out on more sales calls with staff to help close deals with major clients. It meant more late evenings catching up on my administrative tasks, but it was well worth it.
                        <p></p>
                        【范例解析】这个回答将简历中提到的点进行了展开补充，既讲的是简历上没有的内容，又提到了比较积极的工作品格。
                        <p></p>
                        【特别注意】1）尽管是简历上没有的内容，依然要围绕申请的岗位进行回答，不要扯太远。2）真实地表述自己的性格、品格。当你回答自己的隐藏能力/性格时切忌编造，面试官阅人无数，不真实的表述相当于给自己挖坑，容易在追问下露出破绽，适得其反。3）不要讲负面内容，也不要太多废话。此类开放问题比较突兀，在回答前可以先组织语言，不要说我准备讲xxx，直奔主题简练的表达即可。
                        <p></p>
                        Question4: How do you handle your failure
                        <p></p>
                        【解题思路】每个工作中都有可能遇到失败或瓶颈，面试官都想知道求职者面对失败的反应。回答这个问题，你不需要过分详细的解释你是如何解决工作中遇到的问题，而是要表述你在困境中的思考和后续如何去做，同时体现出你不怕挑战的态度。也可以先交代一个你需要承担失败的场景，然后从中吸取的教训和如何确保之后避免相似的问题。
                        <p></p>
                        【回答范例】I look to my professional colleagues in similar jobs and co-workers at my organization for suggestions on how to improve. I am aggressive about taking workshops, training seminars, and online tutorials to upgrade my skills.
                        <p></p>
                        【范例解析】这个回答反应了求职者直面不足，积极向他人寻求帮助的态度。同时也表达了自己如何计划去进行提升，避免同样的错误。如果能在前边描述一个失败的具体案例就更好了。
                        <p></p>
                        【特别注意】1）准备一个失败的案例，并讲述如何吸取教训，如何避免同样的失败。外企面试官喜欢根据你的案例进行深挖，所以一定要准备充分。这个案例最好不要离现在很近，否则面试官可能会将此和未来的工作联系起来。2）一定要讲从中吸取的经验和后续如何做，最好能将结果量化。比如“在我深入研究并做了更细致的预算后，为公司在后续活动上节约了15%的经费。”3）不要耍小聪明，把成功包装成失败。不要试图耍小聪明去讲“本来可以做的更好，但我只做了合格”的案例，面试官会很反感这种行为。
                        <p></p>
                        Question5: Describe your typical work week/Describe a typical day in your job
                        <p></p>
                        【解题思路】这是外企面试常见的一个问题。求职者回答这个问题可以向面试官展示自己以往的工作经验是否与应聘岗位相关，以及个人工作习惯和特质是否与公司吻合。如果求职者在应聘一家“996”公司时候描述了“955”公司的工作习惯，这就会显得不够契合。准备这个问题要在面试前提前了解新公司员工的工作状况，并思考自己当前工作与新工作的联系。
                        <p></p>
                        【回答范例】During a typical work week, one of my biggest tasks is checking in on my staff and assessing progress on various projects. I like to meet first thing on Mondays to discuss our priorities for the week, then meet again in the middle of the week to check progress, and once at the end of the week to discuss goal setting for the next week. I meet with smaller groups of my staff in the middle of the week to troubleshoot any issues. I also attend a weekly meeting where I present my department’s progress to the executive board. On Fridays, I make sure all tasks are completed and I've sent all the necessary communications via email and in person. Finally, I create a list of priorities for next week.
                        <p></p>
                        【范例解析】求职者简洁、清晰地描述了一周的工作内容，时间点交代的很清楚，表现了他是一个做事计划性强，有条理的人。如果能在介绍时加入一些具体的案例细节会更有说服力。
                        <p></p>
                        【特别注意】1）介绍时要结构清晰。最好是通过时间线把各项工作串联起来，切忌想到什么说什么。2）思考原工作与应聘工作的异同点，尽可能多说与现有工作有联系的内容，少讲没有关系的工作内容。
                        <p></p>
                        Question6: Why did you choose your major?
                        <p></p>
                        【解题思路】这个问题主要针对那些应聘实习或初级岗位的求职者。回答这个问题没有绝对对错之分，真实合理即可。但需要注意的是，回答这道问题也是展示你专业技能及兴趣爱好和应聘岗位契合的机会。在准备这道题时，可以先把自己在学校学的重要知识和技能列出来，与应聘岗位进行匹配，把可以匹配到的点在准备的答案中重点突出。
                        <p></p>
                        【回答范例】I chose to be a nutritionist because I have always felt that poor eating habits came from poor knowledge. I wanted to help people get healthy by teaching them about food, not telling them what they can and cannot eat. As an exercise science/nutrition major, I became an expert on getting and staying healthy, and I cannot wait to work with clients. I'm looking forward to putting my excellent interpersonal skills and communication abilities to work, in addition to all that I've learned from my courses.
                        <p></p>
                        【范例解析】这个求职者的回答将所学专业与应聘岗位联系了起来。既表达了他对专业的热爱，也突显了他的主要能力。
                        <p></p>
                        【特别注意】1）把所学专业、对专业的热爱与应聘岗位联系起来。2）表达出对所学专业的热情。每个招聘者都希望能招到对工作充满热情、主动性强的求职者。
                        <p></p>
                        Question7: What is your greatest weakness
                        <p></p>
                        【解题思路】面试官问突然问：说说你的缺点，给求职者施加一种压力，通常是为了看看求职者如何化解这个问题、是否能正确地审视自己，最重要的还是看求职者能否能胜任这个岗位。回答这个问题，你可以准备一些并不影响应聘岗位工作的技能缺陷或能力劣势，同时表明自己正在采取哪些措施进行改进和提升。
                        <p></p>
                        【回答范例】Organization hasn’t always been my strongest point. I’ve always been inclined to prioritize tasks that directly impact the bottom line, and maintaining a pristine desk or an organized inbox didn’t seem to truly move the needle in terms of my output. Over time, I’ve learned that keeping a clean workspace—both physically, and digitally—does support my ability to focus and enhance the efficiency of my workflow. I’ve implemented a time management system that enables me to stay organized without encroaching on my other responsibilities.
                        <p></p>
                        【范例解析】候选人先是明确了一个缺陷，然后交代了自己通过什么样的工作来解决这一缺陷。巧妙地化解了面试官设置的障碍，并把在回答中展示了自己处理问题问题的能力。
                        <p></p>
                        【特别注意】1）不要编造。编造的回答很可能不符合你给面试官的印象，且面试官可能会根据你的答案继续深挖，很容易露馅，没必要在这里冒险。2）不要耍小聪明，把好的点当做坏的点讲。比如有人说自己的弱点是过分认真、钻牛角尖，这在面试官看来是非常幼稚的小把戏。3）不要讲的太多或说自己没有缺点。讲的太多可能影响面试官对你的看法，说自己没有缺点就过分自大了。
                        <p></p>
                        Quesiton8: Do you work well with other people
                        <p></p>
                        【解题思路】面试官想知道你是否能在工作中很好地与他人协作，毕竟糟糕的人际关系处理和团队协作能力会对整个团队带来的巨大的负面影响。求职者应不仅仅只是表达自己乐于与他人合作，更应举出一些与他人协作的经历体现自己在这方面的能力。
                        <p></p>
                        【回答范例】Working on a number of team projects has allowed me to develop my ability to communicate clearly with others, and mediate conflicts between team members. For example, on a recent project, two of my teammates were having trouble reaching an agreement about how to approach an element of the project. I listened to each of their concerns and got everyone to sit down and come up with a solution that would satisfy everyone. Because of my ability to listen to others and mediate conflict, we were able to finish our project ahead of schedule, and even received commendation from our employer for the high quality of our work.
                        <p></p>
                        【范例解析】该候选人的回答给出了一个清楚的案例展示自己沟通和化解分歧等团队协作能力，举例讲让回答非常有信服力。
                        <p></p>
                        【特别注意】1)    团队协作、人际处理可能涉及到的一些能力：激励、帮助他人的能力；化解矛盾、冲突，引导大家达成一致的能力；倾听能力；分工协作的统筹能力；说话艺术等…2)    不要讲负面内容。比如提到因为自己态度问题和别人发生争执，后来化解了，这样可能给面试官带来担忧。
                        <p></p>
                        以上就是小编为大家总结的8个应聘外企岗位时常见的面试问题，外企与国内企业的面试问题同种有异，侧重点和回答思路略有不同，在面试准备时不能完全按照中文面试的思路去做。祝大家早日拿到offer！
