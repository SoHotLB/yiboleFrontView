                        <p>
                            疫情当下，目前有招聘需求的公司几乎都选择了“无接触式招聘”——所有招聘流程都在线上完成。而远程面试——视频/电话面试是其中最重要的环节。对很多还没有回国留学生来讲，大批公司采用远程面试是一个比较好的机会，不用再纠结是否要飞回国面试，极大的节省了开支且提升了求职效率。远程面试和线下面试要注意的问题并不相同，但也没有很多同学想的那么复杂，做好应对策略，把握好“特殊时期”的求职机会。约克学长专门整理了这篇文章，向大家讲解“远程面试那些事儿”，包括面试前的准备工作和面试过程中的注意事项。
                            远程面试要做哪些准备？远程面试的典型形式分为视频面试和电话面试。当你和面试官的接触方式变了，面试技巧也会和面对面接触的场景有些不同。学会利用这种场景变化，可以提高你的面试获胜率。

                        <h3>1. 营造一个专业的面试环境</h3>
                        <p>

                            面试应该是一个专业的、私密的环境，所以一定要找个安静的地方。避免面试沟通时出现其他的打扰情况。如家里有宠物的、小孩等，尽可能做一些避免打扰的准备或提前告知面试官。如果你是视频面试，那还要注意一下周围的光线不能过暗，能够看得清脸。得体的面试着装。在家面试不等于展示你在家的样子。最好着装正式，带淡妆，像互联网行业对穿着要求比较随意，你可以T恤、衬衫自然一些。
                        </p>
                         <h3>  2. 学会利用设备来帮助自己</h3>
                        线上面试的体验有很多不可控的因素，比如电脑没声啊、面试很卡啊，虽然都是些小事儿，但是发生多了也挺影响面试体验的。在面试前，你需要提前控制变量因素，这也反应了你的职场素养高低，是否有规划管理的意识、能妥帖的做好安排。提前安装并测试面试软件、设备音画质量。最好使用耳机。保障交流传达的准确性。在旁边准备一台电脑。可以放上你的电子版简历，准备好的问题以及对应答案list，起到辅助回答的作用。也可以准备纸质版（不要总是去看准备的材料）。手机静音，电脑避免其它软件弹窗
                        <h3>   3. 遇到突击面试，注意以下这些点：</h3>
                        <p>
                        学会记录自己投递的公司和岗位。避免接起电话时都不清楚是哪家公司，JD是什么。电话礼仪很重要。平时大大咧咧惯了，没有打过商务电话的同学，求职期间特别要养成一些基本的礼貌用语习惯，避免踩雷。主动选择面试时间。有一些同学一打电话发现是面试官就很紧张，装作说我没空之类，会显得非常不礼貌。就算你当下没有做好准备，也可以先告诉面试官“很抱歉现在不是很方便，是否可以约一个更适合的时间。比如今天下午2点，是否方便呢？”，主动去向面试官约时间，通常面试官都能表示理解。
                        </p>
