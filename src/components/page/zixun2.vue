                         <h3>  1. 不可取少的远程面试礼仪</h3>

                        <p>远程面试的情绪传达是比较弱的，不如面对面沟通那么直接、强烈。这一端你的情绪要足够饱满、去带动面试官，从而留下一个好印象。

                            保持微笑，表达重视：即使是电话面试，你在这边的微笑，也是能从语言中听出来的。换位思考，考虑对方的感受和体验：比如问一问是否声音清晰，切忌打断他人，最好是听清楚问题后，再回答。激昂的情绪：避免让面试官觉得你很麻木，问一句回一句，特别是在电话面试时，因为面试官看不到你的表情，只能从语气来判断你的状态。眼神交流：有些同学视频面试时不知道该往哪里看，或者总是去看自己准备的答题材料，眼神飘忽不定，这可能是重要的减分项。最好根据不同的设备提前找准自己在视频通话时的眼神落点，起到像面对面Eye contact一样的作用。学会表达感谢：面试结束的时候可以说“非常感谢您提供面试机会”
                        </p>
                        <h3>   2. 视频面试答题技巧：</h3>

                        远程面试，少了很多诸如面对面的感觉、临场状态、肢体语言等影响条件，面试官更容易把考察精力集中在求职者的回答上。这就要求求职者必须注意两大问题：理解面试官问题的意图和有逻辑、有条理的回答问题。理解面试官的问题：抛开专业问题不谈，常见的面试问题都有会考察的点，回答问题之前弄明白问题的意图非常重要。不要急于回答面试官提出的问题，给自己3-5s中的停顿思考面试官提问的意图。 举例面试官问：“你未来的职业规划是什么？”是为了考察求职者的兴趣是否与岗位匹配，是否具有稳定性…因此就可以对应的进行回答。 再比如面试官问：“你为什么能胜任这个岗位”是想看你对这次机会是否足够重视，是否对岗位做了足够的了解，是否有特别的能力能承担相应的工作…

                        <h3>    3.如何有条理、有逻辑的回答面试问题：</h3>

                        <p>    建议多使用STAR法则回答问题，既能让你组织答案有迹可循，也能让面试官听起来非常“舒服”。STAR法则，即Situation(情景)、Task(任务)、Action(行动)和Result(结果)。作为求职者，熟练使用STAR法则回答面试官的问题则会为你的答案加分不少。  拿面试常见问题举个例：讲讲你实习经历中的成功经历/讲一件你最成功的事， 先要一句话讲清楚你要讲什么事，（例：我举一个我进入某公司实习的第二个月，就定下双倍完成销售考核业绩的目标，且最终超额完成的故事）然后用STAR法则将故事完整的叙述下来。Situation & Task： 我在某公司实习时，每个实习生都有月销售目标。由于我被推举为实习生组长，我在第二个月时给自己定下了两倍于其他实习生的考核业绩的目标，希望发挥带头作用。Action：一开始我有点为自己定了如此高的目标后悔，因为从来没有实习生能完成这样的成绩。后来我明白不能坐以待毙，除了积极阅读像《阿里铁军》这样的书籍，去网上搜集并学习相关的销售技巧，还积极去找老员工取经，通过专门请教、日常闲聊等方式学习他们的销售经验，并自己改进了一套前期电话联系、中期微信巩固、后期线下达成的销售方法，效果非常明显。Result：最终依靠这个方法超额完成了两倍考核业绩的目标，受到了很多前辈的认可。我的方法论也被领导在部门周会上推广。这件事使我明白其实我有非常大的潜力，要勇于突破，不能安于现状。

                            以上就是约克学长为大家梳理的远程/视频面试的攻略。虽然远程面试比起线下面试可控性没有那么强，且大家接触的机会少，但只要掌握好上述远程面试的这些技巧，相信面试可以在这个特殊时期能够“弯道超车”！
                        </p>
